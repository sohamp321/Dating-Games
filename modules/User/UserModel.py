class User:
    def __init__(self, index, name, gender, userid, password, avatar, bio, country, DOB, partner ,messages) -> None:
        self.index = index
        self.name = name
        self.gender = gender
        self.userid = userid
        self.password = password
        self.avatar = avatar
        self.bio = bio
        self.country = country
        self.DOB = DOB
        self.partner = partner
        self.messages = messages
